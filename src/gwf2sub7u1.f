C9-------SUM NO-DELAY COMPACTION IN ALL MODEL LAYERS.
       IF(NNDF) THEN
        DO 105 KQ=1,NNDB
        K=LN(KQ)
        OCLAY(K)=.TRUE.
        LOCT=(KQ-1)*NCR
        LOCTH=(K-1)*NCR
        N=0
        DO 100 IJ=1,NCR
        N=N+1
        LOC2=LOCT+N
        LOC2H=LOCTH+N
        BUFF(LOC2H)=BUFF(LOC2H)+SUB(LOC2)
  100   CONTINUE
  105   CONTINUE
       ENDIF
C
C10------SUM DELAY COMPACTION IN ALL MODEL LAYERS.
       IF(NDF) THEN
        DO 115 KQ=1,NDB
        K=LDN(KQ)
        OCLAY(K)=.TRUE.
        LOCT=(KQ-1)*NCR
        LOCTH=(K-1)*NCR
        N=0
        DO 110 IJ=1,NCR
        N=N+1
        LOC2=LOCT+N
        LOC2H=LOCTH+N
        BUFF(LOC2H)=BUFF(LOC2H)+DCOM(LOC2)
  110   CONTINUE
  115   CONTINUE
       ENDIF
      ENDIF
C
C11-----PRINT COMPACTION BY LAYER.
      IF(OCFLGS(3,NNSTP)) THEN
       DO 120 KL=1,NLAY
       IF(.NOT.OCLAY(KL)) GO TO 120
       KKL=KL
       LOC2H=(KKL-1)*NCR+1
       IF(IUNSTR.EQ.0)THEN
       IF(ISBOCF(2).LT.0) CALL ULAPRS(BUFF(LOC2H),TEXT(2),KSTP,KPER,
     1           NCOL,NROW,KKL,-ISBOCF(2),IOUT)
       IF(ISBOCF(2).GE.0) CALL ULAPRW(BUFF(LOC2H),TEXT(2),KSTP,KPER,
     1            NCOL,NROW,KKL,ISBOCF(2),IOUT)
        ELSE
        IF(ISBOCF(2).NE.0) CALL ULAPRU(BUFF(LOC2H),TEXT(2),KSTP,KPER,
     1      LOC2,LOC2+NCR-1,KKL,IABS(ISBOCF(2)),IOUT,PERTIM,TOTIM,NODES)
        ENDIF
  120  CONTINUE
      ENDIF
C
C12-----STORE COMPACTION BY LAYER.
      IF(OCFLGS(4,NNSTP)) THEN
       DO 125 KL=1,NLAY
       IF(.NOT.OCLAY(KL)) GO TO 125
       KKL=KL
       LOC2H=(KKL-1)*NCR+1
       IF(IUNSTR.EQ.0)THEN
       CALL ULASAV(BUFF(LOC2H),TEXT(2),KSTP,KPER,PERTIM,TOTIM,NCOL,
     1             NROW,KKL,ISBOCU(2))
        ELSE
       CALL ULASAVU(BUFF(LOC2H),TEXT(2),KSTP,KPER,PERTIM,TOTIM,LOC2,
     1    LOC2+NCR-1,KQ,ISBOCU(2),NODES)
        ENDIF
  125  CONTINUE
      ENDIF
C
C13----CALCULATE VERTICAL DISPLACEMENT.
      IF(OCFLGS(7,NNSTP).OR.OCFLGS(8,NNSTP)) THEN
       NL1=NLAY-1
       IF(NLAY.GT.1) THEN
        DO 140 KL=NL1,1,-1
        LOCT3=(KL-1)*NCR
        N=0
        DO 135 IJ=1,NCR
        N=N+1
        BUFF(LOCT3+N)=BUFF(LOCT3+N)+BUFF(LOCT3+N+NCR)
  135   CONTINUE
  140   CONTINUE
       ENDIF
C
C14-----PRINT VERTICAL DISPLACEMENT FOR ALL MODEL LAYERS.
       IF(OCFLGS(7,NNSTP)) THEN
        DO 145 KL=1,NLAY
        KKL=KL
       LOC2H=(KKL-1)*NCR+1
       IF(IUNSTR.EQ.0)THEN
        IF(ISBOCF(4).LT.0) CALL ULAPRS(BUFF(LOC2H),TEXT(5),KSTP,KPER,
     1            NCOL,NROW,KKL,-ISBOCF(4),IOUT)
        IF(ISBOCF(4).GE.0) CALL ULAPRW(BUFF(LOC2H),TEXT(5),KSTP,KPER,
     1             NCOL,NROW,KKL,ISBOCF(4),IOUT)
        ELSE
        IF(ISBOCF(4).NE.0) CALL ULAPRU(BUFF(LOC2H),TEXT(5),KSTP,KPER,
     1      LOC2,LOC2+NCR-1,KKL,IABS(ISBOCF(4)),IOUT,PERTIM,TOTIM,NODES)
        ENDIF
  145   CONTINUE
       ENDIF
C
C15-----SAVE VERTICAL DISPLACEMENT FOR ALL MODEL LAYERS.
       IF(OCFLGS(8,NNSTP)) THEN
        DO 150 KL=1,NLAY
        KKL=KL
        LOC2H=(KKL-1)*NCR+1
        IF(IUNSTR.EQ.0)THEN
        CALL ULASAV(BUFF(LOC2H),TEXT(5),KSTP,KPER,PERTIM,TOTIM,NCOL,
     1              NROW,KKL,ISBOCU(4))
        ELSE
       CALL ULASAVU(BUFF(LOC2H),TEXT(5),KSTP,KPER,PERTIM,TOTIM,LOC2,
     1    LOC2+NCR-1,KQ,ISBOCU(4),NODES)
        ENDIF
  150   CONTINUE
       ENDIF
      ENDIF
C
C16-----PRINT CRITICAL HEAD FOR SYSTEMS OF NO-DELAY INTERBEDS.
C16-----STORAGE.
      IF(OCFLGS(9,NNSTP).OR.OCFLGS(10,NNSTP)) THEN
       IF(NNDF) THEN
       DO 155 NL=1,NLAY
       OCLAY(NL)=.TRUE.
  155  CONTINUE
        DO 160 KQ=1,NNDB
        K=LN(KQ)
        LOC2=(KQ-1)*NCR+1
        LOC2H=(K-1)*NCR+1
        IF(.NOT.OCLAY(K)) GO TO 160
        NSYS=1
        ILSYS(1)=KQ
        IF(KQ.LT.NNDB) THEN
         KS1=KQ+1
         DO 152 KS=KS1,NNDB
         IF(LN(KS).EQ.K) THEN
          NSYS=NSYS+1
          ILSYS(NSYS)=KS
         ENDIF
  152    CONTINUE
        ENDIF
        OCLAY(K)=.FALSE.
        IF(OCFLGS(9,NNSTP)) THEN
         WRITE(IOUT,154) (ILSYS(NS),NS=1,NSYS)
  154  FORMAT(/,1X,' SYSTEM OR SYSTEMS OF NO-DELAY BEDS:',20I3)
         IF(IUNSTR.EQ.0)THEN
         IF(ISBOCF(5).LT.0) CALL ULAPRS(HC(LOC2),TEXT(6),KSTP,KPER,NCOL,
     1            NROW,K,-ISBOCF(5),IOUT)
         IF(ISBOCF(5).GE.0) CALL ULAPRW(HC(LOC2),TEXT(6),KSTP,KPER,NCOL,
     1             NROW,K,ISBOCF(5),IOUT)
        ELSE
        IF(ISBOCF(5).NE.0) CALL ULAPRU(HC(LOC2),TEXT(6),KSTP,KPER,
     1        LOC2,LOC2+NCR-1,K,IABS(ISBOCF(5)),IOUT,PERTIM,TOTIM,NODES)
        ENDIF
        ENDIF
        IF(OCFLGS(10,NNSTP)) THEN
         IF(IUNSTR.EQ.0)THEN
         CALL ULASAV(HC(LOC2),TEXT(6),KSTP,KPER,PERTIM,TOTIM,NCOL,
     1            NROW,K,ISBOCU(5))
        ELSE
       CALL ULASAVU(HC(LOC2),TEXT(6),KSTP,KPER,PERTIM,TOTIM,LOC2,
     1    LOC2+NCR-1,KQ,ISBOCU(5),NODES)
        ENDIF
        ENDIF
  160   CONTINUE
       ENDIF
      ENDIF
C
C17-----PRINT CRITICAL HEAD FOR ALL SYSTEMS OF DELAY INTERBED.
      IF(OCFLGS(11,NNSTP).OR.OCFLGS(12,NNSTP)) THEN
       IF(NDF) THEN
        LOC4=0
        DO 190 KQ=1,NDB
        K=LDN(KQ)
        LOCT=(KQ-1)*NCR
        N=0
        DO 180 IJ=1,NCR
        N=N+1
        BUFF(IJ)=ZERO
        LOC2=LOCT+N
        IF(RNB(LOC2).LT.1.0) GO TO 180
        LOC4=LOC4+NN
        BUFF(IJ)=DHC(LOC4)
  180   CONTINUE
        IF(OCFLGS(11,NNSTP)) THEN
         WRITE(IOUT,82) KQ
         IF(IUNSTR.EQ.0)THEN
         IF(ISBOCF(6).LT.0) CALL ULAPRS(BUFF,TEXT(7),KSTP,KPER,
     1             NCOL,NROW,K,-ISBOCF(6),IOUT)
         IF(ISBOCF(6).GE.0) CALL ULAPRW(BUFF,TEXT(7),KSTP,KPER,
     1              NCOL,NROW,K,ISBOCF(6),IOUT)
        ELSE
        IF(ISBOCF(6).NE.0) CALL ULAPRU(BUFF,TEXT(7),KSTP,KPER,
     1        LOC2,LOC2+NCR-1,K,IABS(ISBOCF(6)),IOUT,PERTIM,TOTIM,NODES)
        ENDIF
        ENDIF
        IF(OCFLGS(12,NNSTP)) THEN
         IF(IUNSTR.EQ.0)THEN
         CALL ULASAV(BUFF,TEXT(7),KSTP,KPER,PERTIM,TOTIM,NCOL,
     1            NROW,KQ,ISBOCU(6))
        ELSE
       CALL ULASAVU(BUFF,TEXT(7),KSTP,KPER,PERTIM,TOTIM,LOC2,
     1    LOC2+NCR-1,KQ,ISBOCU(6),NODES)
        ENDIF
        ENDIF
  190   CONTINUE
       ENDIF
      ENDIF
C
C18-----PRINT VOLUMETRIC BUDGET FOR SYSTEMS OF DELAY INTERBEDS
      IF(NDF.AND.IBDPR) THEN
       WRITE(IOUT,230) KSTP,KPER
       SUMSV=ZERO
       SUMBV=ZERO
       SUMSR=ZERO
       SUMBR=ZERO
       DO 200 KQ=1,NDB
       DISCV=ZERO
       DISCR=ZERO
       SUMV=DVB(KQ,1)+DVB(KQ,2)
       SUMR=DVB(KQ,3)+DVB(KQ,4)
