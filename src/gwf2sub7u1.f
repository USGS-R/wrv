     1        LOC2,LOC2+NCR-1,K,IABS(ISBOCF(3)),IOUT,PERTIM,TOTIM,NODES)
        ENDIF
        ENDIF
        IF(OCFLGS(6,NNSTP)) THEN
          IF(IUNSTR.EQ.0)THEN
         CALL ULASAV(DCOM(LOC2),TEXT(4),KSTP,KPER,PERTIM,TOTIM,NCOL,
